test("installHost refuses to replace a Cursor symlink that points elsewhere", async () => {
  await withTempHome(async (home) => {
    const target = join(home, ".cursor", "plugins", "local", "pi-spawner");
    await mkdir(join(home, ".cursor", "plugins", "local"), { recursive: true });
    await symlink("/tmp/some-other-plugin", target, "dir");

    const result = await installHost("cursor");

    assert.equal(result.ok, false);
    assert.match(result.detail, /already points to a different plugin/);
  });
});

async function withTempHome(callback: (home: string, state: string) => Promise<void>): Promise<void> {
  const previousHome = process.env.HOME;
  const previousPiSpawnerHome = process.env.PI_SPAWNER_HOME;
  const home = await mkdtemp(join(tmpdir(), "pi-spawner-home-"));
  const state = await mkdtemp(join(tmpdir(), "pi-spawner-state-"));
  try {
    process.env.HOME = home;
    process.env.PI_SPAWNER_HOME = state;
    await callback(home, state);
  } finally {
    if (previousHome === undefined) {
      delete process.env.HOME;
    } else {
      process.env.HOME = previousHome;
    }
    if (previousPiSpawnerHome === undefined) {
      delete process.env.PI_SPAWNER_HOME;
    } else {
      process.env.PI_SPAWNER_HOME = previousPiSpawnerHome;
    }
    await rm(home, { recursive: true, force: true });
    await rm(state, { recursive: true, force: true });
  }
}

async function withTempInstallEnv(callback: (home: string, state: string, bin: string, logPath: string) => Promise<void>): Promise<void> {
  const previousPath = process.env.PATH;
  const previousFakeLog = process.env.FAKE_LOG;
  await withTempHome(async (home, state) => {
    const bin = await mkdtemp(join(tmpdir(), "pi-spawner-bin-"));
    const logPath = join(home, "commands.log");
    try {
      process.env.PATH = `${bin}:${previousPath || ""}`;
      process.env.FAKE_LOG = logPath;
      await callback(home, state, bin, logPath);
    } finally {
      if (previousPath === undefined) {
        delete process.env.PATH;
      } else {
        process.env.PATH = previousPath;
      }
      if (previousFakeLog === undefined) {
        delete process.env.FAKE_LOG;
      } else {
        process.env.FAKE_LOG = previousFakeLog;
      }
      await rm(bin, { recursive: true, force: true });
    }
  });
}

async function writeExecutable(path: string, contents: string): Promise<void> {
  await writeFile(path, contents, "utf8");
  await chmod(path, 0o755);
}
