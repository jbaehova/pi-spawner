test("ensureCodexPersonalPlugin refuses to overwrite unrelated plugin directory", async () => {
  const previousHome = process.env.HOME;
  const previousPiSpawnerHome = process.env.PI_SPAWNER_HOME;
  const home = await mkdtemp(join(tmpdir(), "pi-spawner-home-"));
  const state = await mkdtemp(join(tmpdir(), "pi-spawner-state-"));
  try {
    process.env.HOME = home;
    process.env.PI_SPAWNER_HOME = state;
    const pluginRoot = join(home, "plugins", "pi-spawner");
    await mkdir(pluginRoot, { recursive: true });
    await writeFile(join(pluginRoot, "README.md"), "not generated by pi-spawner\n", "utf8");

    await assert.rejects(
      () => ensureCodexPersonalPlugin("pi-spawner"),
      /already exists and is not a Pi Spawner plugin/
    );
  } finally {
    if (previousHome === undefined) {
      delete process.env.HOME;
    } else {
      process.env.HOME = previousHome;
    }
    if (previousPiSpawnerHome === undefined) {
      delete process.env.PI_SPAWNER_HOME;
    } else {
      process.env.PI_SPAWNER_HOME = previousPiSpawnerHome;
    }
    await rm(home, { recursive: true, force: true });
    await rm(state, { recursive: true, force: true });
  }
});

test("ensureCodexPersonalPlugin refuses same-name non-Pi Spawner plugin manifests", async () => {
  await withTempHome(async (home) => {
    const pluginRoot = join(home, "plugins", "pi-spawner");
    await mkdir(join(pluginRoot, ".codex-plugin"), { recursive: true });
    await writeFile(
      join(pluginRoot, ".codex-plugin", "plugin.json"),
      `${JSON.stringify({ name: "pi-spawner", description: "A different local plugin" }, null, 2)}\n`,
      "utf8"
    );

    await assert.rejects(
      () => ensureCodexPersonalPlugin("pi-spawner"),
      /already contains a different Codex plugin/
    );
  });
});

test("ensureCodexPersonalPlugin backs up malformed personal marketplace JSON", async () => {
  await withTempHome(async (home) => {
    const marketplaceDir = join(home, ".agents", "plugins");
    await mkdir(marketplaceDir, { recursive: true });
    await writeFile(join(marketplaceDir, "marketplace.json"), "{ invalid json\n", "utf8");

    await ensureCodexPersonalPlugin("pi-spawner");

    const entries = await readdir(marketplaceDir);
    assert.ok(entries.some((entry) => entry.startsWith("marketplace.json.invalid-")));
    const marketplace = JSON.parse(await readFile(join(marketplaceDir, "marketplace.json"), "utf8"));
    assert.equal(marketplace.name, "personal");
    assert.equal(marketplace.plugins[0].name, "pi-spawner");
  });
});

