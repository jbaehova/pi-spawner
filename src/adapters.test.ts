test("installHost repairs a broken Codex pi-spawner marketplace before installing", async () => {
  await withTempInstallEnv(async (_home, _state, bin, logPath) => {
    await writeExecutable(join(bin, "codex"), `#!/bin/sh
printf '%s\\n' "$*" >> "$FAKE_LOG"
if [ "$1 $2" = "plugin list" ]; then
  echo 'Error: failed to load marketplace(s):' >&2
  echo '- \`pi-spawner\` at /tmp/pi-spawner: marketplace root does not contain a supported manifest' >&2
  exit 1
fi
if [ "$1 $2 $3" = "plugin marketplace remove" ]; then
  echo '{"removed":true}'
  exit 0
fi
if [ "$1 $2" = "plugin add" ]; then
  echo '{"installed":true}'
  exit 0
fi
exit 2
`);

    const result = await installHost("codex", { repairBrokenCodexMarketplace: true });

    assert.equal(result.ok, true);
    const log = await readFile(logPath, "utf8");
    assert.match(log, /plugin marketplace remove pi-spawner --json/);
    assert.match(log, /plugin add pi-spawner@personal --json/);
  });
});

